package co.com.coomeva.ele.modelo;

public class EleAsesorLicoDTO {
	private String CDGO;
	private int anosRetiro;
	private String EPR_CDGO;
	
	public String getCDGO() {
		return CDGO;
	}
	public void setCDGO(String cdgo) {
		CDGO = cdgo;
	}
	public int getAnosRetiro() {
		return anosRetiro;
	}
	public void setAnosRetiro(int anosRetiro) {
		this.anosRetiro = anosRetiro;
	}
	public String getEPR_CDGO() {
		return EPR_CDGO;
	}
	public void setEPR_CDGO(String epr_cdgo) {
		EPR_CDGO = epr_cdgo;
	}
	
}
